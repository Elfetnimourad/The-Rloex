let hh = document.querySelector("#hr");
let hm = document.querySelector("#hm");
let hs = document.querySelector("#hs");

setInterval(function SetClock() {
  let date = new Date();
  let sr = date.getSeconds() / 60;
  let mr = (sr + date.getMinutes()) / 60;
  let hr = (mr + date.getHours()) / 12;
  setRotation(hh, hr);
  setRotation(hm, mr);
  setRotation(hs, sr);
  function setRotation(e, rr) {
    e.style.setProperty("--rotation", rr * 360);
  }
});
SetClock();
